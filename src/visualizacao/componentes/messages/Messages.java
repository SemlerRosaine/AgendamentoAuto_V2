package visualizacao.componentes.messages;

import javax.swing.JOptionPane;

/**
 *
 * @author Jhonnatan
 */
public class Messages {
    
    public static void showInfoMessage(String message){
        JOptionPane.showMessageDialog(null, message, "Informação", JOptionPane.INFORMATION_MESSAGE);
    }
    
    public static void showWarningMessage(String message){
        JOptionPane.showMessageDialog(null, message, "Atenção", JOptionPane.WARNING_MESSAGE);
    }
    
    public static void showErrorMessage(String message){
        JOptionPane.showMessageDialog(null, message, "Erro", JOptionPane.ERROR_MESSAGE);
    }
}
